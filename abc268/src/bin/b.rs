use std::mem::swap;
#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        S:Chars,T:Chars
    }
    if S.len() > T.len() {
        println!("No");
        return;
    }
    for i in 0..S.len() {
        if S[i] != T[i] {
            println!("No");
            return;
        }
    }
    println!("Yes");
}

fn main() {
    solve();
}
