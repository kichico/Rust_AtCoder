#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        s:String
    }
    for i in (0..s.len()).step_by(4) {
        let c = &s[i..i + 4];
        if c == "post" {
            println!("{}", i / 4 + 1);
            return;
        }
    }
    println!("none");
}
fn main() {
    solve();
}
