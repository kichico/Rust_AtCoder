#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        s:Chars
    }
    for i in 0..s.len() {
        if i == 3 && s[i] != '-' {
            println!("No");
            return;
        } else if i != 3 && !s[i].is_ascii_digit() {
            println!("No");
            return;
        }
    }
    println!("Yes");
}
fn main() {
    solve();
}
