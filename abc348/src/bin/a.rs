#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize
    }
    let mut ans = Vec::new();
    for i in 1..=n {
        let c = if i % 3 == 0 { 'x' } else { 'o' };
        ans.push(c);
    }
    println!("{}", ans.iter().join(""));
}
fn main() {
    solve();
}
