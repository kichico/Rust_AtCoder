#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        q:usize
    }
    let mut st = vec![0; 100];
    for _ in 0..q {
        input! {t:usize}
        if t == 1 {
            input! {x:usize}
            st.push(x);
        } else {
            println!("{}", st.pop().unwrap());
        }
    }
}
fn main() {
    solve();
}
