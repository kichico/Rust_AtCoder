#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        s:Chars
    }
    let it = s
        .iter()
        .enumerate()
        .filter(|x| x.0 % 2 == 1 && x.1 == &'1')
        .count();
    let ans = if it >= 1 { "No" } else { "Yes" };
    println!("{}", ans);
}
fn main() {
    solve();
}
