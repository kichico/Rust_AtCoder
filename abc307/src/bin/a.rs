#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,a:[i64;7*n]
    }
    let mut ans = vec![0; n];
    for i in 0..n {
        let mut sum = 0;
        for j in 0..7 {
            sum += a[i * 7 + j];
        }
        ans[i] = sum;
    }
    println!("{}", ans.iter().join(" "));
}
fn main() {
    solve();
}
