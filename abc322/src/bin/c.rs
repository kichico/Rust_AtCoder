#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,m:usize,a:[usize;m]
    }
    for i in 0..n {
        println!("{}", a[a.lower_bound(&(i + 1))] - (i + 1));
    }
}
fn main() {
    solve();
}
