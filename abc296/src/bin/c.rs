#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,x:i64,mut a:[i64;n]
    }
    a.sort();
    for i in 0..n {
        let p = a.lower_bound(&(a[i] - x));
        if p < n && a[i] - a[p] == x {
            println!("Yes");
            return;
        }
    }
    println!("No");
}
fn main() {
    solve();
}
