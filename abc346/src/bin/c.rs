#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,k:usize,a:[usize;n]
    }
    let mut ans = k * (k + 1) / 2;
    let vals: BTreeSet<usize> = BTreeSet::from_iter(a.into_iter());
    for v in vals.range(0..=k) {
        ans -= v;
    }
    println!("{}", ans);
}
fn main() {
    solve();
}
