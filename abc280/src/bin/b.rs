#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,s:[i64;n]
    }
    let mut a = vec![0; n];
    a[0] = s[0];
    let mut currentsum = a[0];
    for i in 1..n {
        a[i] = s[i] - currentsum;
        currentsum += a[i];
    }
    println!("{}", a.iter().join(" "));
}
fn main() {
    solve();
}
