#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize
    }
    let mut cnt: HashSet<usize> = HashSet::new();
    for a in 2..=n.sqrt() {
        let mut b = 2;
        while a.pow(b) <= n {
            cnt.insert(a.pow(b));
            b += 1;
        }
    }
    println!("{}", n - cnt.len());
}
fn main() {
    solve();
}
