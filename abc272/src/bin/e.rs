#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,a:[i64;n]
    }
    let mut min_value = std::i64::MAX;
    for i in 0..n {
        if a[i] >= 0 {
            min_value = min(a[i], min_value);
        }
    }
    if min_value > 0 {}
}

fn main() {
    solve();
}
