#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,x:i64,a:[i64;n]
    }
    let ans = a.into_iter().filter(|v| *v != x).collect::<Vec<i64>>();
    if ans.len() > 0 {
        println!("{}", ans.iter().join(" "));
    }
}
fn main() {
    solve();
}
