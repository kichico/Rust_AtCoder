#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}

#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,t:usize,p:usize,mut l:[usize;n]
    }
    let num = l.iter().filter(|x| x >= &&t).count();
    if num >= p {
        println!("0");
        return;
    }
    l.sort();
    l.reverse();
    let need = l.iter().take(p).last().unwrap();
    println!("{}", t - need);
}
fn main() {
    solve();
}
