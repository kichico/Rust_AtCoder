#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:usize,days:[i64;n]
    }
    let mut half = (days.iter().sum::<i64>() + 1) / 2;
    for (month, day) in enumerate(days) {
        if half > day {
            half -= day;
        } else {
            println!("{} {}", month + 1, half);
            return;
        }
    }
}
fn main() {
    solve();
}
