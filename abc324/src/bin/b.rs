#[allow(dead_code)]
#[allow(non_snake_case)]
fn to_char(x: i64) -> char {
    return std::char::from_digit(x as u32, 10).unwrap();
}
#[allow(non_snake_case)]
fn solve() {
    input! {
        n:i128
    }
    for x in 0..=60 {
        for y in 0..=40 {
            let val = 2.pow(x) * 3.pow(y);
            if val == n {
                println!("Yes");
                return;
            }
        }
    }
    println!("No");
}
fn main() {
    solve();
}
